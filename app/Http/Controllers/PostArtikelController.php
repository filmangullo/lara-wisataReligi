    {
        $artikel = new Artikel;

        $artikel->judul_artikel = $request->judul_artikel;
        $artikel->isi_artikel = $request->isi_artikel;
        $artikel->jenis_golongan = $request->jenis_golongan;
        $artikel->save();

        return redirect()->back();
    }

